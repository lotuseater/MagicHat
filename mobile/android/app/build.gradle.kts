fun relayPinset(version: String): String {
    val propertyName = "MAGICHAT_ANDROID_RELAY_PINSET_${version.uppercase().replace('-', '_')}"
    return (project.findProperty(propertyName) as String?)
        ?: System.getenv(propertyName)
        ?: ""
}

